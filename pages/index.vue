<script>
import { Navigation } from 'swiper/modules';
import { Swiper, SwiperSlide } from 'swiper/vue';
import 'swiper/css';
import lottie from 'lottie-web/build/player/lottie';
export default {
    props: {
        slideId: String,
    },
    data() {
        return {
            active: 0,
            thumbItems: [],
            timeout: null,
            images: [
                { src: "/img/slide1.png", alt: "" },
                { src: "/img/slide2.png", alt: "" },
                { src: "/img/slide3.png", alt: "" },
                { src: "/img/slide4.png", alt: "" },
            ],
            currentIndex: 0,
            modules: [Navigation],
            navigation2: {
                nextEl: '.next',
                prevEl: '.prev',
            },

            breakpoints: {
                320: {
                    slidesPerView: 1,
                    spaceBetween: 20,

                },
                1025: {
                    slidesPerView: 3,
                    spaceBetween: 30,
                }
            },
        }
    },
    mounted() {
        this.addThumbItems();
        this.autoSlide();
        this.loadAnimation()
        this.loadAnimation2()
    },
    methods: {
        activeSlide(index) {
            this.active = index;
            this.autoSlide();
        },
        prev() {
            if (this.active > 0) {
                this.activeSlide(this.active - 1);
            } else {
                this.activeSlide(this.images.length - 1);
            }
        },
        next() {
            if (this.active < this.images.length - 1) {
                this.activeSlide(this.active + 1);
            } else {
                this.activeSlide(0);
            }
        },
        addThumbItems() {
            this.thumbItems = Array.from({ length: this.images.length });
        },
        autoSlide() {
            clearTimeout(this.timeout);
            this.timeout = setTimeout(this.next, 5000);
        },
        async loadAnimation() {
            try {
                const response = await fetch("/animation/animation.json"); // Замените путь на путь к вашему файлу animation.json
                const animationData = await response.json();

                const container = this.$refs.jsonContainer;
                lottie.loadAnimation({
                    container: container,
                    renderer: "svg",
                    loop: true,
                    autoplay: true,
                    animationData: animationData,
                });
            } catch (error) {
                console.error("Ошибка при загрузке или отображении анимации:", error);
            }
        },
        async loadAnimation2() {
            try {
                const response = await fetch("/animation/animation.json"); // Замените путь на путь к вашему файлу animation.json
                const animationData = await response.json();

                const container = this.$refs.jsonContainer2;
                lottie.loadAnimation({
                    container: container,
                    renderer: "svg",
                    loop: true,
                    autoplay: true,
                    animationData: animationData,
                });
            } catch (error) {
                console.error("Ошибка при загрузке или отображении анимации:", error);
            }
        },
    },
}
</script>
